function fetchRandomUser() {
  return new Promise((resolve, reject) => {
    https
      .get('https://randomuser.me/api/?randomapi', (resp) => {
        let data = '';
        resp.on('data', (chunk) => {
          data += chunk;
        });
        resp.on('end', () => {
          const user = JSON.parse(data).results[0];
          resolve(user);
        });
      })
      .on('error', (err) => {
        reject(err);
      });
  });
}

