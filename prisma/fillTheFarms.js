async function main() {
  for (const farm of farmData) {
    const randomUser = await fetchRandomUser();

    const newUser = await prisma.user.create({
      data: {
        email: randomUser.email,
        name: `${randomUser.name.first} ${randomUser.name.last}`,
        username: randomUser.login.username,
        role: ['user'],
        type: ['visitor', 'farmer', 'worker'],
        phone: randomUser.phone,
        profileImage: randomUser.picture.thumbnail,
        picture: randomUser.picture.large,
        image: randomUser.picture.large,
      },
    });

    await prisma.farmProfile.create({
      data: {
        userId: newUser.id,
        name: farm.t,
        web: farm.url,
        phone: farm.m, // Included as per your request
        latitude: parseFloat(farm.lt),
        longitude: parseFloat(farm.ln),
        farmSlug: slugify(farm.t, { lower: true, strict: true }),
      },
    });
  }

  // console.log('FarmProfile seeding complete');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
